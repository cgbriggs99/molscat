      subroutine Hdata
C  Copyright (C) 2025 J. M. Hutson & C. R. Le Sueur
C  Distributed under the GNU General Public License, version 3
C
C  MODULE CONTAINING PARAMETERS OF A PAIR OF TIEMANN-STYLE
C  POTENTIAL CURVES OF AN ALKALI DIMER
C  UNITS USED ARE CM-1 AND POWERS OF ANGSTROM
C
C  POTENTIAL 1 IS SINGLET X, POTENTIAL 2 IS TRIPLET A
C
      USE physical_constants, ONLY: bohr_to_Angstrom
      use pot_data_Hannover
      implicit none
C
      if (allocated(potnam)) deallocate (potnam)
      allocate (character(128) :: potnam)

      POTNAM =
     X ' K2 potential from Tiemann et al., &
     doi: https://doi.org/10.1103/PhysRevResearch.2.013366'
C
C  PRINT CONTROL (FOR POTENTIAL ROUTINE ONLY)
C  0 = SILENT, 1 = TITLE, 2 = INTERNALLY CALCULATED PARAMETERS
C
      IPRINT = 2
C
C  NUMBER OF TERMS IN POWER SERIES FOR CENTRAL REGION
C  (NOTE: HIGHEST POWER + 1 BECAUSE ARRAYS START AT 0)
C
      NA = (/23,15/)
C
C  EXPANSION COEFFICIENTS:
C
      if (allocated(a)) deallocate (a)
      allocate (a(0:maxval(na)-1,2))

      A = RESHAPE((/
     X -4450.9007703d0,
     X  0.159877863995326747d0,
     X  0.141337574101676037d5,
     X  0.107669620493846905d5,
     X -0.331314023322698995d4,
     X -0.163943210499613087d5,
     X -0.216334200177141829d5,
     X -0.384655804768731250d5,
     X -0.768229889574501722d5,
     X  0.157896664088991121d6,
     X  0.833691806464401074d6,
     X -0.115890452663354226d7,
     X -0.653607110081680864d7,
     X  0.487172809603480622d7,
     X  0.308101362964722812d8,
     X -0.863340173933527432d7,
     X -0.811804637748816609d8,
     X  0.492251670364311151d7,
     X  0.121156746090629265d9,
     X  0.280059277888290165d7,
     X -0.968951931944736689d8,
     X -0.314874358611015789d7,
     X  0.324661526246530302d8,

     a -255.0214692d0,
     a -0.013405598929310479d0,
     a  0.153940442323125171d4,
     a -0.626944977828736569d3,
     a -0.147039918194012284d4,
     a  0.238628331428504282d3,
     a -0.121465057044283844d4,
     a -0.131024472517054273d5,
     a  0.410390478256789502d5,
     a  0.585609645570106004d5,
     a -0.316660644987405278d6,
     a  0.178579875710784399d6,
     a  0.690085326716458891d6,
     a -0.116538893384502688d7,
     a  0.541518493723396794d6,
     a  0.D0, 0.D0, 0.D0, 0.D0, 0.D0, 0.D0, 0.D0,
     a  0.D0 /), SHAPE(A))
C
C     LOGICAL VARIABLES TO CONTROL CONTINUITY MATCHING:
C
      MATCHL = .TRUE.
      MATCHV = .TRUE.
      MATCHD = .TRUE.
C
C     IF (MATCHL), ADJUST A(0) TO MAKE POTENTIAL  CONTINUOUS AT RLR
C     IF (MATCHL), ADJUST ASR  TO MAKE POTENTIAL  CONTINUOUS AT RSR
C     IF (MATCHD), ADJUST BSR  TO MAKE DERIVATIVE CONTINUOUS AT RSR
C     IF .FALSE., USE THE CORRESPONDING INPUT VALUE UNCHANGED AND
C     PRINT THE RESULTING MISMATCH FOR INFORMATION.
C
C     RM AND B ARE USED IN THE DEFINITION OF XI.
C     RSR AND RLR ARE SHORT-RANGE AND LONG-RANGE MATCHING POINTS.
C     PARAMETERS OF SHORT-RANGE POTENTIAL ASR + BSR*R**NSR.
C
      RM    = (/3.9243617, 5.7347289/)
      B     = (/-0.39, -0.40/)
      RSR   = (/2.87, 4.755/)
      RLR   = (/12.00, 12.00/)
      NSR   = (/12, 6/)
      ASR   = (/-0.2600158561d4, -0.7009379657d3/)
      BSR   = (/0.8053173040d9, 0.80690073665d7/)
      ALPSR = (/0.D0,0.D0/)
C
C  DISPERSION COEFFICIENTS
C
      C6  = 0.1892338370d8
      C8  = 0.5706799528d9
      C10 = 0.1853042723d11
C
C  EXTRA -C26/R**26 TERM PRESENT IN LONG-RANGE POTENTIAL FOR RB2
C
      ! NEX = 26
      ! CEX = 0.2819810D26
C
C  PARAMETERS OF EXCHANGE POTENTIAL
C  NOTE THAT BETA AND GAMMA ARE CONVENTIONALLY LINKED.
C  THREE OPTIONS ARE IMPLEMENTED HERE:
C        GAMBET = 0: USE INPUT VALUES UNCHANGED
C        GAMBET = 1: CALCULATE GAMMA FROM BETA
C        GAMBET = 2: CALCULATE BETA FROM GAMMA
C  IF CHANGED, THE INPUT VALUE IS PRINTED FOR COMPARISON
C
      GAMBET = 1
      EXSIGN = (/-1.D0, +1.D0/)
      AEX    = 0.90092159d4
      GAMMA  = 5.19500
      BETA   = 2.13539
C
C  DISTANCE UNITS TO BE USED BY MOLSCAT/BOUND:
C  1.D0 FOR ANGSTROM, 0.529... FOR BOHR, ETC.
C
      RUNITM = 1.0
      LENUNT = 'ANGSTROM'
C
      return
      end
